function detectLocale(): Locale {
  const lang = navigator.language.toLowerCase()
  if (lang.startsWith('zh')) return 'zh'
  if (lang.startsWith('ja')) return 'ja'
  return 'en'
}

let currentLocale: Locale = (localStorage.getItem('nokori-locale') as Locale) || detectLocale()

export function getLocale(): Locale {
  return currentLocale
}

export function setLocale(locale: Locale) {
  currentLocale = locale
  localStorage.setItem('nokori-locale', locale)
}

export function t(key: string, params?: Record<string, string | number>): string {
  let text = translations[currentLocale]?.[key] ?? translations.en[key] ?? key
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v))
    })
  }
  return text
}

/** Localized maintenance job name; falls back to raw key if unknown. */
export function maintenanceJobLabel(jobKey: string): string {
  const i18nKey = `lifecycle.job.${jobKey}`
  const label = t(i18nKey)
  return label === i18nKey ? jobKey : label
}

export function lz(en: string | null | undefined, zh: string | null | undefined): string {
  return (getLocale() === 'zh' && zh ? zh : en) ?? ''
}

